export const mediaReducer = (state = initStateNewsList, action) => {
  if (action.type === 'SET_MEDIALIST') {
    return {
      ...state,
      mediaList: action.value,
    };
  }
  if (action.type === 'SET_MEDIA') {
    return {
      ...state,
      media: action.value,
    };
  }
  if (action.type === 'SET_MEDLIST') {
    return {
      ...state,
      medList: action.value,
    };
  }
  if (action.type === 'SET_MED') {
    return {
      ...state,
      med: action.value,
    };
  }
  return state;
};
